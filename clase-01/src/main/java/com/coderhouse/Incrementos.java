package com.coderhouse;

public class Incrementos {

	public static void main(String[] args) {
		int miNum = 0;
		System.out.println("1) El valor de mi numero es: " + miNum);
		
		miNum = miNum +1;
		System.out.println("2) El valor de mi numero es: " + miNum);
		
		miNum++;
		System.out.println("3) El valor de mi numero es: " + miNum);
		
		miNum--;
		System.out.println("4) El valor de mi numero es: " + miNum);
	}

}
